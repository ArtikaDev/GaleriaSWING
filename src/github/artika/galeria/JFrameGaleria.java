    private void jListArchivosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jListArchivosKeyPressed
        if (!rutaTemp.equals("")){
            // Le quitamos la funcionalidad que tiene por defecto JList en las teclas up y down
            InputMap inputMap = jListArchivos.getInputMap(JComponent.WHEN_FOCUSED);
            inputMap.put(KeyStroke.getKeyStroke(evt.VK_UP, 0), "none");
            inputMap.put(KeyStroke.getKeyStroke(evt.VK_DOWN, 0), "none");
        
            int code = evt.getKeyCode();
            int posicionActual = jListArchivos.getSelectedIndex();
            int ultimoElemento = jListArchivos.getModel().getSize() - 1;
            
            if (code == KeyEvent.VK_UP && posicionActual == 0){
                jListArchivos.setSelectedIndex(ultimoElemento);
            }else if (code == KeyEvent.VK_UP){
                jListArchivos.setSelectedIndex(posicionActual - 1);
            }else if (code == KeyEvent.VK_DOWN && posicionActual == ultimoElemento){
                jListArchivos.setSelectedIndex(0);
            }else if (code == KeyEvent.VK_DOWN ){
                jListArchivos.setSelectedIndex(posicionActual + 1);
            }
        }
    }//GEN-LAST:event_jListArchivosKeyPressed

    
