    private void redimensionarImagenSegunLabel(){
        ImageIcon imagenIcon = new ImageIcon(rutaTemp + "\\" + jListArchivos.getSelectedValue());
        Image imagen = imagenIcon.getImage();
        Image escalaImagen = imagen.getScaledInstance(jLabelImagen.getWidth(), jLabelImagen.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imagenEscalada = new ImageIcon(escalaImagen);
        jLabelImagen.setIcon(imagenEscalada);
    }
