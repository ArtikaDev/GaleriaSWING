    private void jButtonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnteriorActionPerformed
        if (!"".equals(rutaTemp)){
            int posActual = jListArchivos.getSelectedIndex();
            if (posActual != 0){
                jListArchivos.setSelectedIndex(posActual - 1);
            }else{
                jListArchivos.setSelectedIndex(jListArchivos.getModel().getSize() - 1);
            }
        }
    }//GEN-LAST:event_jButtonAnteriorActionPerformed

    private ArrayList<String> listarArchivos(String ruta){
        ArrayList<String> listaArchivos = new ArrayList<>();

        File folder = new File(ruta);
        File[] files = folder.listFiles();

        if (files != null) {
            for (File file : files) {
                if (file.getName().toLowerCase().endsWith(".jpg")){
                    listaArchivos.add(file.getName());
                }
                
            }
        }
        return listaArchivos;
    }
    
    private void anyadirListaAJList(ArrayList<String> listaArchivos){
        DefaultListModel<String> modelo = new DefaultListModel<>();

        for (String archivo : listaArchivos) {
            modelo.addElement(archivo);
        }
        
        jListArchivos.setModel(modelo);
    }
    
    
    
