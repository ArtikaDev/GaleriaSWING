    private void jListArchivosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListArchivosValueChanged
        if (!evt.getValueIsAdjusting()){
            redimensionarImagenSegunLabel();
            SwingUtilities.invokeLater(() -> { //Ejecuta la siguiente orden despues de que se actualice el jList
                jListArchivos.ensureIndexIsVisible(jListArchivos.getSelectedIndex());
            });
        }
    }//GEN-LAST:event_jListArchivosValueChanged

    // Al llegar al final si se pulsa la seleccion pasa al principio
    
    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        if (!"".equals(rutaTemp)){
            int posActual = jListArchivos.getSelectedIndex();
            int posFinal = jListArchivos.getModel().getSize() - 1;
            if (posActual != posFinal){
                jListArchivos.setSelectedIndex(posActual + 1);
            }else{
                jListArchivos.setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

